function applyResult(hud, result) {
  let next = hud;
  if (Array.isArray(result.dismiss) && result.dismiss.length) {
    const drop = new Set(result.dismiss);
    next = next.filter(c => !drop.has(c.id));
  }
  if (Array.isArray(result.cards) && result.cards.length) {
    next = [...result.cards, ...next].slice(0, HUD_CAP);
  }
  return next;
}

function connect(url) {
  return new Promise((resolve, reject) => {
    const ws = new WebSocket(url);
    const to = setTimeout(() => { ws.terminate(); reject(new Error('connect timeout')); }, 15_000);
    ws.on('open', () => { clearTimeout(to); resolve(ws); });
    ws.on('error', (e) => { clearTimeout(to); reject(e); });
  });
}

function sendBatch(ws, batch, hud) {
  const requestId = uuid();
  return new Promise((resolve, reject) => {
    const t0 = Date.now();
    const timer = setTimeout(() => { ws.off('message', onMsg); reject(new Error(`batch timeout ${requestId}`)); }, BATCH_TIMEOUT_MS);
    function onMsg(raw) {
      let env; try { env = JSON.parse(raw.toString()); } catch { return; }
      if (env.type === 'assistant_result' && env.requestId === requestId) {
        clearTimeout(timer); ws.off('message', onMsg);
        resolve({ cards: env.cards || [], dismiss: env.dismiss || [], latencyMs: Date.now() - t0 });
      }
    }
    ws.on('message', onMsg);
    ws.send(JSON.stringify({ type: 'assistant', requestId, wearerText: batch.w || '', interlocutorText: batch.i || '', activeCards: hud, model: MODEL }));
  });
}
function send(ws, obj) { ws.send(JSON.stringify(obj)); }

// latin-letter detection for English-leak flag (ignore common tech tokens)
const TECH = /\b(SLA|ROI|CI|CRM|IT|SaaS|B2B|CDP|API|outbox|senior|middle|Java|Spring|exactly-once|dedup|99\.\d|SOC|ISO|GDPR|MOU)\b/gi;
function looksEnglish(s) {
  const stripped = (s||'').replace(TECH, '').replace(/[A-Za-z]/g, m => '');
  // count remaining latin letters after removing tech tokens
  const rem = (s||'').replace(TECH, '');
  const latin = (rem.match(/[A-Za-z]/g) || []).length;
  const cyr = (rem.match(/[А-Яа-яЁё]/g) || []).length;
  return latin > 8 && latin > cyr; // mostly-latin note => likely english
}

async function run() {
  const lines = [];
  const log = (s='') => { lines.push(s); };
  let ws;
  try { ws = await connect(WS_URL); } catch (e) { log(`CONNECT FAILED: ${e.message}`); fs.writeFileSync(OUT, lines.join('\n')); console.log('CONNECT_FAILED'); process.exit(2); }
  log('COPILOT STRESS TEST — RUSSIAN WORST-CASE — ' + new Date().toISOString());
  log('Endpoint: ' + WS_URL + '  model=' + MODEL);

  const allLat = [];
  const meta = [];

  for (let s = 0; s < SCENARIOS.length; s++) {
    const sc = SCENARIOS[s];
    const deviceId = `copilot-ru-${s+1}-${crypto.randomBytes(3).toString('hex')}`;
    send(ws, { type: 'identify', deviceId, deviceType: 'glasses' });
    await sleep(250);
    send(ws, { type: 'assistant_new' });
    await sleep(250);

    log('\n' + '='.repeat(78));
    log(`SCENARIO ${s+1}: ${sc.name}  (deviceId=${deviceId})`);
    log('='.repeat(78));

    let hud = [];
    let fired = 0, empty = 0;
    const scLat = [];
    const allNotes = [];
    let engLeak = 0;

    for (let b = 0; b < sc.batches.length; b++) {
      const batch = sc.batches[b];
      let res;
      try { res = await sendBatch(ws, batch, hud); }
      catch (e) { log(`\n[batch ${b+1}] ERROR: ${e.message}`); continue; }
      allLat.push(res.latencyMs); scLat.push(res.latencyMs);

      log('');
      log(`--- batch ${b+1}/${sc.batches.length}  (${res.latencyMs}ms) ---`);
      log(`Wearer:       ${batch.w || '(silent)'}`);
      log(`Interlocutor: ${batch.i || '(silent)'}`);

      const newCards = res.cards || [];
      const dismiss = res.dismiss || [];
      if (newCards.length === 0 && dismiss.length === 0) {
        empty++;
        log('  -> CARDS: (none)');
      } else {
        if (newCards.length) fired++;
        for (const c of newCards) {
          allNotes.push(c.note || '');
          const leak = looksEnglish(c.note) ? '  [!! ENGLISH-LEAK ?]' : '';
          if (looksEnglish(c.note)) engLeak++;
          log(`  -> CARD [${c.id}] kind=${c.kind}`);
          log(`       heard: ${c.heard || ''}`);
          log(`       note:  ${c.note || ''}${leak}`);
        }
        if (dismiss.length) log(`  -> DISMISS: ${dismiss.join(', ')}`);
        hud = applyResult(hud, res);
      }
      log(`  HUD now: [${hud.map(c=>c.id).join(', ')}]`);
    }

    log(`\nSCENARIO ${s+1} SUMMARY: batches_with_cards=${fired}, empty=${empty}, total=${sc.batches.length}, english_leaks=${engLeak}`);
    const p = (arr,q)=>{ if(!arr.length) return 0; const a=[...arr].sort((x,y)=>x-y); return a[Math.min(a.length-1, Math.floor(q*(a.length-1)))]; };
    log(`  latency ms: min=${Math.min(...scLat)} p50=${p(scLat,.5)} p90=${p(scLat,.9)} max=${Math.max(...scLat)}`);
    meta.push({ name: sc.name, fired, empty, total: sc.batches.length, engLeak, scLat, notes: allNotes });
  }

  log('\n' + '='.repeat(78));
  log('OVERALL');
  log('='.repeat(78));
  const p = (arr,q)=>{ if(!arr.length) return 0; const a=[...arr].sort((x,y)=>x-y); return a[Math.min(a.length-1, Math.floor(q*(a.length-1)))]; };
  log(`Latency across ${allLat.length} batches: min=${Math.min(...allLat)} p50=${p(allLat,.5)} p90=${p(allLat,.9)} max=${Math.max(...allLat)}`);
  for (const m of meta) log(`- ${m.name}: cards in ${m.fired}/${m.total} batches, ${m.empty} empty, english_leaks=${m.engLeak}`);

  fs.writeFileSync(OUT, lines.join('\n'));
  ws.close();
  await sleep(300);

  // emit compact machine summary to stdout (small)
  const out = {
    overall: { n: allLat.length, min: Math.min(...allLat), p50: p(allLat,.5), p90: p(allLat,.9), max: Math.max(...allLat) },
    scenarios: meta.map(m => ({ name: m.name, fired: m.fired, empty: m.empty, total: m.total, engLeak: m.engLeak,
      lat: { min: Math.min(...m.scLat), p50: p(m.scLat,.5), p90: p(m.scLat,.9), max: Math.max(...m.scLat) } }))
  };
  console.log(JSON.stringify(out, null, 2));
}
run().catch(e => { console.error('FATAL', e); process.exit(1); });
