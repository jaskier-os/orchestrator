// ---- HUD state helper ----------------------------------------------------
function applyResult(hud, result) {
  let next = hud;
  if (Array.isArray(result.dismiss) && result.dismiss.length) {
    const drop = new Set(result.dismiss);
    next = next.filter(c => !drop.has(c.id));
  }
  if (Array.isArray(result.cards) && result.cards.length) {
    next = [...result.cards, ...next].slice(0, HUD_CAP);
  }
  return next;
}

function fmtList(arr) { return arr.length ? `[${arr.map(c => `${c.id}:${c.kind || '?'}`).join(', ')}]` : '[]'; }

// ---- WS plumbing ---------------------------------------------------------
function connect(url) {
  return new Promise((resolve, reject) => {
    const opts = url.startsWith('wss') ? { rejectUnauthorized: false } : {};
    const ws = new WebSocket(url, opts);
    const to = setTimeout(() => { ws.terminate(); reject(new Error('connect timeout')); }, 15_000);
    ws.on('open', () => { clearTimeout(to); resolve(ws); });
    ws.on('error', (e) => { clearTimeout(to); reject(e); });
  });
}

function sendBatch(ws, batch, hud) {
  const requestId = uuid();
  return new Promise((resolve, reject) => {
    const t0 = Date.now();
    const timer = setTimeout(() => {
      ws.off('message', onMsg);
      reject(new Error(`batch timeout (requestId=${requestId})`));
    }, BATCH_TIMEOUT_MS);
    function onMsg(raw) {
      let env;
      try { env = JSON.parse(raw.toString()); } catch { return; }
      if (env.type === 'assistant_result' && env.requestId === requestId) {
        clearTimeout(timer);
        ws.off('message', onMsg);
        resolve({ cards: env.cards || [], dismiss: env.dismiss || [], latencyMs: Date.now() - t0 });
      }
    }
    ws.on('message', onMsg);
    ws.send(JSON.stringify({
      type: 'assistant',
      requestId,
      wearerText: batch.w || '',
      interlocutorText: batch.i || '',
      activeCards: hud,
      model: MODEL
    }));
  });
}

function send(ws, obj) { ws.send(JSON.stringify(obj)); }

// ---- Run -----------------------------------------------------------------
async function run() {
  log(`[harness] connecting to ${WS_URL}`);
  let ws;
  try { ws = await connect(WS_URL); }
  catch (e) { log(`[harness] CONNECT FAILED: ${e.message}`); process.exit(2); }
  log('[harness] connected');

  for (let s = 0; s < SCENARIOS.length; s++) {
    const sc = SCENARIOS[s];
    const deviceId = `copilot-ru-${s + 1}-${crypto.randomBytes(3).toString('hex')}`;
    send(ws, { type: 'identify', deviceId, deviceType: 'glasses' });
    await sleep(250);
    send(ws, { type: 'assistant_new' });
    await sleep(250);

    log(`\n================= СЦЕНАРИЙ ${s + 1}: ${sc.name} (deviceId=${deviceId}) =================`);
    let hud = [];
    let fired = 0, empty = 0;
    const allNotes = [];

    for (let b = 0; b < sc.batches.length; b++) {
      const batch = sc.batches[b];
      let res;
      try { res = await sendBatch(ws, batch, hud); }
      catch (e) { log(`[батч ${b + 1}] ОШИБКА: ${e.message}`); continue; }

      log(`\n[батч ${b + 1}] (${res.latencyMs}ms)`);
      log(`  Носитель: ${batch.w ? '"' + batch.w + '"' : '(молчит)'}`);
      log(`  Собеседник: ${batch.i ? '"' + batch.i + '"' : '(молчит)'}`);

      const newCards = res.cards || [];
      const dismiss = res.dismiss || [];
      if (newCards.length === 0 && dismiss.length === 0) {
        empty++;
        log(`  -> карточки: (нет)  | dismiss: (нет)  | HUD: ${fmtList(hud)}`);
      } else {
        if (newCards.length) fired++;
        for (const c of newCards) {
          log(`  -> КАРТОЧКА [${c.id}] kind=${c.kind} heard="${c.heard}"`);
          log(`        note: ${c.note}`);
          allNotes.push(c.note);
        }
        if (dismiss.length) log(`  -> DISMISS: ${dismiss.join(', ')}`);
        hud = applyResult(hud, res);
        log(`  | HUD: ${fmtList(hud)}`);
      }
    }
    log(`\n  ИТОГ СЦЕНАРИЯ: карточек выдано в ${fired} батчах, пусто в ${empty}.`);
  }

  ws.close();
  await sleep(300);
  log('\n[harness] готово, ws закрыт');

  fs.writeFileSync(OUT_FILE, lines.join('\n') + '\n', 'utf8');
  console.log(`[harness] full transcript written to ${OUT_FILE}`);
}

run().catch(e => { log(`[harness] fatal ${e && e.message}`); fs.writeFileSync(OUT_FILE, lines.join('\n') + '\n', 'utf8'); process.exit(1); });
